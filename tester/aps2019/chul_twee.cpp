#include<stdio.h>
#include<tuple>
#include<math.h>

std::pair<int, int> x,y;
int map[1000][1000];
int cnt, ans;

int main(void){
	int N;
	scanf("%d", &N);
	for(int i=0; i<N; i++){
		for(int j=0; j<N; j++){
			scanf("%*c%c", &map[i][j]);
            map[i][j] -= '0';
			if(map[i][j] == 2) x = {i,j};
			else if(map[i][j] == 5) y = {i,j};
		}
	}
	if(hypot(x.first-y.first, x.second-y.second) > 4.5){
		if(x.first > y.first){
			int temp = x.first; x.first = y.first; y.first = temp;
		}
		if(x.second > y.second){
			int temp = x.second; x.second = y.second; y.second = temp;
		}
		for(int i=x.first; i<=y.first; i++){
			for(int j=x.second; j<=y.second; j++){
				cnt += map[i][j]==1;
			}
		}
		ans = cnt>2;
	}	
	printf("%d", ans);
}

